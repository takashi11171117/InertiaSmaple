<template>
    <div>
        <div class="mb-4">
            <inertia-link class="flex items-center group py-3" href="/">
                <icon name="dashboard" class="w-4 h-4 mr-2" :class="isUrl('') ? 'fill-white' : 'fill-indigo-light group-hover:fill-white'" />
                <div :class="isUrl('') ? 'text-white' : 'text-indigo-lighter group-hover:text-white'">Dashboard</div>
            </inertia-link>
        </div>
    </div>
</template>

<script>
	import Icon from '@/Shared/Icon'
	export default {
		components: {
			Icon,
		},
		props: {
			url: String,
		},
		methods: {
			isUrl(...urls) {
				if (urls[0] === '') {
					return this.url === ''
				}
				return urls.filter(url => this.url.startsWith(url)).length
			},
		},
	}
</script>